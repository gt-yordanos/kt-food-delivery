import mongoose from 'mongoose';

const restaurantOwnerSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const RestaurantOwner = mongoose.model('RestaurantOwner', restaurantOwnerSchema);

export { RestaurantOwner };